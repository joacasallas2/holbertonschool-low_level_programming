#include "3-calc.h"

/**
 * op_add - Function that adds two integers
 * @a: The first integer
 * @b: The first integer
 * Return: The result
 */
int op_add(int a, int b) { return (a + b); }
/**
 * op_sub - Function that adds two integers
 * @a: The first integer
 * @b: The first integer
 * Return: The result
 */
int op_sub(int a, int b) { return (a - b); }
/**
 * op_mul - Function that adds two integers
 * @a: The first integer
 * @b: The first integer
 * Return: The result
 */
int op_mul(int a, int b) { return (a * b); }
/**
 * op_div - Function that adds two integers
 * @a: The first integer
 * @b: The first integer
 * Return: The result
 */
int op_div(int a, int b) { return (a / b); }
/**
 * op_mod - Function that adds two integers
 * @a: The first integer
 * @b: The first integer
 * Return: The result
 */
int op_mod(int a, int b) { return (a % b); }
